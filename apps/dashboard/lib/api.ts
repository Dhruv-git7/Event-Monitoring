const API = process.env.NEXT_PUBLIC_API_URL || 'http://localhost:4002'
async function req(path: string, options: RequestInit = {}) { const res = await fetch(`${API}${path}`, { ...options, credentials: 'include', headers: { 'content-type': 'application/json', ...(options.headers || {}) } }); if (!res.ok) { let msg = `HTTP ${res.status}`; try { msg = (await res.json()).error || msg } catch {} throw new Error(msg) } return res.status === 204 ? null : res.json() }
